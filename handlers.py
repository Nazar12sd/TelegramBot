                f"–ü–æ–ª—É—á–∏–ª –≤–∞—à–µ —Å–æ–æ–±—â–µ–Ω–∏–µ: \"{message_text}\"\n\n"
                f"–°–ø–∞—Å–∏–±–æ –∑–∞ —Å–æ–æ–±—â–µ–Ω–∏–µ! –Ø –±–∞–∑–æ–≤—ã–π –±–æ—Ç –∏ –ø–æ–∫–∞ —É—á—É—Å—å –æ—Ç–≤–µ—á–∞—Ç—å –Ω–∞ —Ä–∞–∑–Ω—ã–µ –∑–∞–ø—Ä–æ—Å—ã. "
